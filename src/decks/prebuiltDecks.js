/**
 * デッキIDのリストを取得
 */
export const getDeckIds = () => Object.keys(PREBUILT_DECKS);

/**
 * デッキ情報を取得
 */
export const getDeckInfo = (deckId) => PREBUILT_DECKS[deckId] || null;

/**
 * デッキ選択用のオプションリストを取得
 */
export const getDeckOptions = () => {
  return Object.values(PREBUILT_DECKS).map(deck => ({
    id: deck.id,
    name: deck.name,
    description: deck.description,
  }));
};
